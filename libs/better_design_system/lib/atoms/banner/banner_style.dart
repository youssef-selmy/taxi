/// Enum representing different visual styles for a banner.
///
/// - [soft]: A soft style with minimal emphasis, often with subtle background and borders.
/// - [outline]: A style with a border around the banner, providing a more defined look.
/// - [fill]: A solid background style, filling the entire area of the banner.
enum BannerStyle {
  soft, // A minimal, subtle style with softer visuals.
  outline, // A style with a border, offering more definition.
  fill, // A filled style with a solid background color.
}
