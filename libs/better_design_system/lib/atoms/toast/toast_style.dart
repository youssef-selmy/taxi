/// An enum that defines the different styles of the toast.
///
/// - [soft]: A soft toast style with subtle background color and rounded edges.
/// - [outline]: A toast with an outline border, giving it a more defined and minimalist look.
/// - [fill]: A solid-filled toast style with a stronger background color for more emphasis.
enum ToastStyle {
  soft, // A subtle style with soft background and minimalistic appearance.
  outline, // A style with a border outline and a more defined edge.
  fill, // A solid background style, emphasizing the content.
}
